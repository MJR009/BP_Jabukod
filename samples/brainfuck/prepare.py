# Program to translate
TRANSLATE = reverse_input



map = {
    ">": "RIGHT",
    "<": "LEFT",
    "+": "INC",
    "-": "DEC",
    ".": "OUT",
    ",": "IN",
    "[": "LBR",
    "]": "RBR"
}

def main():
    if len(TRANSLATE) > 1000:
        print("PROGRAM TOO LONG")
        exit(0)

    program = "int program[1000] = {"

    for instr in TRANSLATE:
        program += map[instr]
        program += ", "

    program = program[:-2]
    program += "};"

    print(program)

main()
